package patternPrograms;

public class BoxPattern {
	public static void main(String[] args) {
		//printing rows, print lines
		for(int i=1;i<=4;i++) {
			//printing columns, print *
			for(int j=1;j<=4;j++) {
				System.out.print("* ");
			}
			System.out.println();
		}
	}
}

